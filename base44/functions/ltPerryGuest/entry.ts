Deno.serve(async (req) => {
  try {
    const { messages } = await req.json();

    if (!Array.isArray(messages) || messages.length === 0) {
      return Response.json({ error: 'messages array required' }, { status: 400 });
    }

    const base44 = createClientFromRequest(req);

    // Keep only last 6 messages to minimize token payload
    const recentMessages = messages
      .filter((m) => m.role === 'user' || m.role === 'assistant')
      .slice(-6);

    const history = recentMessages
      .map((m) => `${m.role === 'user' ? 'User' : 'Lt. Perry'}: ${m.content}`)
      .join('\n\n');

    // Compact prompt: system context + recent history only
    const prompt = `${SYSTEM_PROMPT}\n\nCONVERSATION:\n${history}\n\nLt. Perry (reply concisely, 1-3 sentences max unless detail is required):`;

    const reply = await base44.asServiceRole.integrations.Core.InvokeLLM({ prompt, model: 'gpt_5_mini' });

    return Response.json({ reply });
  } catch (error) {
    return Response.json({ error: error.message }, { status: 500 });
  }
});